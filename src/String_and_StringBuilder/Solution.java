    public static String test(){
        String s = "codeleet";
        int[] indices = {4,5,6,7,0,2,1,3};
        StringBuilder ans = new StringBuilder();
        for (int i = 0; i < indices.length; i++) {
            ans.append(s.charAt(i));
        }
        ans.append(s.charAt(0));
        return (String)ans;
    }

